var initSocket = require('./socket').initSocket;
var $ = require('jquery');
var pubsub = require('pubsub-js');

// onMessageCallback is a function with one parameter that is the JSON results from the speech to text (STT) api
exports.handleMicrophone = function(token, model, mic, callback, onMessageCallback) {
  if (model.indexOf('Narrowband') > -1) {
    var err = new Error('Microphone transcription cannot accomodate narrowband models, '+
      'please select another');
    callback(err, null);
    return false;
  }

  pubsub.publish('clearscreen');

  // Test out websocket
  var baseString = '';
  var baseJSON = '';

  pubsub.subscribe('showjson', function() {
    var $resultsJSON = $('#resultsJSON');
    $resultsJSON.empty();
    $resultsJSON.append(baseJSON);
  });

  var options = {};
  options.token = token;
  options.message = {
    'action': 'start',
    'content-type': 'audio/l16;rate=16000',
    'interim_results': true,
    'continuous': true,
    'word_confidence': true,
    'timestamps': true,
    'max_alternatives': 3,
    'inactivity_timeout': 600
  };
  options.model = model;

  function onOpen(socket) {
    console.log('Mic socket: opened');
    callback(null, socket);
  }

  function onListening(socket) {

    mic.onAudio = function(blob) {
      if (socket.readyState < 2) {
        socket.send(blob);
      }
    };
  }

  function onMessage(msg) {
    if (msg.results) {
      onMessageCallback(msg);
      // baseString = display.showResult(msg, baseString, model);
      // baseJSON = display.showJSON(msg, baseJSON);
    }
  }

  function onError() {
    console.log('Mic socket err: ', err);
  }

  function onClose(evt) {
    console.log('Mic socket close: ', evt);
  }

  initSocket(options, onOpen, onListening, onMessage, onError, onClose);
};
